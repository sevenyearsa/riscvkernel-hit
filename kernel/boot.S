# boot.S

# 声明我们使用的是我们之前在链接脚本里定义的那个极其特殊的段
.section .text.init

# 导出 _start 符号，链接器需要找它作为整个系统的绝对入口
.global _start

_start:
    # =========================================================
    # 动作一：只留一个“天选之子”（屏蔽多核）
    # =========================================================
    # QEMU 启动时，所有 CPU 核心 (hart) 都会同时从这里开始跑。
    # 对于早期的 OS，并发是灾难。我们先只让主核（Hart 0）工作。
    csrr    t0, mhartid       # 读取当前 CPU 的硬件线程 ID (Hardware Thread ID)
    bnez    t0, park          # 如果 ID 不为 0 (Branch if Not Equal to Zero)，让他去一边凉快

    # =========================================================
    # 动作二：清理作案现场（清空 .bss 段）
    # =========================================================
    # C 语言的规范要求所有未初始化的全局变量默认为 0。
    # 这里我们用到前面在 os.ld 中定义的边界符号。
    la      a0, bss_start     # 把 bss 段的起始物理地址装入 a0 寄存器
    la      a1, bss_end       # 把 bss 段的结束物理地址装入 a1 寄存器
    bgeu    a0, a1, 2f        # 如果 start >= end，说明没有 bss 段，跳过清零 (跳到标签 2)

1:  # 清零循环开始
    sd      zero, (a0)        # 将 zero 寄存器 (永远为0) 的 64 位值存入 a0 指向的内存
    addi    a0, a0, 8         # 指针向前移动 8 字节 (64位)
    bltu    a0, a1, 1b        # 如果还没到结尾，跳回标签 1 (1b 的 b 代表 backward)

2:  # 清零结束

    # =========================================================
    # 动作三：铺设 C 语言的“温床”（设置栈指针 SP）
    # =========================================================
    # C 语言的局部变量和函数调用链全靠栈来维系。
    # RISC-V 的栈是“向下生长”的（从高地址向低地址延伸）。
    la      sp, stacks        # 拿到我们预留的栈空间的起始地址
    li      t0, 4096          # 假设我们给主核分配 4KB 的栈
    add     sp, sp, t0        # 把 sp 指针推到这 4KB 的最顶端！

    # =========================================================
    # 动作四：交接控制权（跳入 C 世界）
    # =========================================================
    call    os_main           # 绝对跳转到 C 语言编写的主函数
    
    # 理论上 os_main 永远不该返回。如果它崩溃退出了，我们就让主核也去休息
    j       park

    # =========================================================
    # 终极归宿：休眠死循环
    # =========================================================
park:
    wfi                       # Wait For Interrupt (等待硬件中断，让 CPU 进入低功耗睡眠)
    j       park              # 如果被意外唤醒，继续死循环休眠


# =========================================================
# 附录：手动在物理内存中划出一块地作为“栈”
# =========================================================
.section .bss.stack           # 放在 bss 段中
.align 4                      # 保证内存地址按 16 字节对齐 (2^4)，提升存取性能
.global stacks
stacks:
    .skip 4096                # 用 .skip 指令霸占 4096 字节的连续物理空间