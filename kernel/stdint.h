#ifndef STDINT_H
#define STDINT_H
// ==========================================
// 造物主的手工数据类型 (基于 64 位 RISC-V 架构)
// ==========================================
typedef unsigned char      uint8_t;   // 8 位
typedef unsigned short     uint16_t;  // 16 位
typedef unsigned int       uint32_t;  // 32 位
typedef unsigned long long uint64_t;  // 64 位

#endif